package com.gn.practice;
public class Practice2 {
	public static void main(String[] args) {
		
		int reasult = 100;
		reasult += 3;
		System.out.println(reasult); //103
		reasult -= 10;
		System.out.println(reasult); //93
		reasult *= 2;
		System.out.println(reasult); //186
		reasult /= 3;
		System.out.println(reasult); //62
		reasult %= 3;
		System.out.println(reasult); //2
		
		// 홀수, 짝수
		// 1,3,5... -> 2로 나눴을 때 나머지 1
		// 2,4,6... -> 2로 나눴을 때 나머지 0
		
	}

}
