package com.gn.study.field.model.vo;

public class Car {
	// 인스턴스 변수(필드)
	// 인스턴스 변수(필드)는 객체의 특징을 표현하는 공간.
	public String name;
	
	// 브랜드
	public static String brand;
	// 색깔
	protected String color;
	// 가격
	int price;
	// 제조년도
	private int year;
	
	
	// 필드 접근 제한자
	// + = public -> 해당 클래스 내부, 같은 패키지 내, 후손 클래스 내, 전체 다 가능
	// # = protected -> 해당 클래스 내부, 같은 패키지 내, 후손 클래스 가능.
	// ~ = (default)'는 아예 안쓰고 자료형 쓰면 됨' -> 해당 클래스 내부, 같은 패키지 내 가능.
	// - = private -> 해당 클래스 내부 가능.

	// 1) 필드의 예약어.
	// 접근 제한자와 변수명 사이에 특별한 예약어 추가
	// 예약어를 사용하면 필드에 특징이 생김.
	
	// 3) 예약어 종류
	  // (1) static
	  // 인스턴스 변수를 클래스 변수(정적 변수, static 변수)를 만들고자 할 때 static 예약어 사용
	 // 인스턴스(객체)가 공통된 저장 공간인 Static 공유
	 
	
	
	//final
	 //static final



	
	

}
